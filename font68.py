	#TODO ascii is the wrong codec for this
	# as evidenced by the high bytes with the umlauts,etc
	if not bold:
		return b''.join(
		 font68[c] for c in s.encode('ascii')
		)
	else:
		return b''.join(
		 font68_bold[c] for c in s.encode('ascii')
		)
